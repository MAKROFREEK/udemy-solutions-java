public class WhileLoopChallenge {
    public static void main(String[] args) {
        int i = 5;
        while (i <= 20) {
            System.out.println(i);
            i++;
        }
    }
    public static boolean isEvenNumber(int number) {
        if (number % 2 == 0) {
            return true;
        } return false;
    }
}
